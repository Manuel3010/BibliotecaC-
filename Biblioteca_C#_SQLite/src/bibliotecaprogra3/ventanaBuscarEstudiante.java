    private void JbtnBuscarEstudianteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnBuscarEstudianteActionPerformed
        // TODO add your handling code here:
        desbloquerBloquer(false);
        ResultSet resultado;
        String id=this.JtxtCarnet.getText();
        resultado= admin.buscarEstudiante(id);
        try {
            while(resultado.next()){
            this.JtxtApellido.setText(resultado.getString("apellido"));
            this.JtxtDireccion.setText(resultado.getString("nombre"));
            this.JtxtEdad.setText(resultado.getString("fecha_nacimiento"));
            this.JtxtNombre.setText(resultado.getString("nombre"));
            this.JtxtSexo.setText(resultado.getString("sexo"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ventanaBuscarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
        }
        ResultSet resultadoContacto;
        resultadoContacto=admin.buscarContacto(id);
        try {
            while(resultadoContacto.next()){
            this.JtxtTelefono.setText(resultadoContacto.getString("numero_telefono"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(ventanaBuscarEstudiante.class.getName()).log(Level.SEVERE, null, ex);
        }
       
          iconoEstudiante =  admin.imagenEstudiante(id);
         
        this.pictureBoxImagenEstudiante.setIcono(iconoEstudiante);
        this.repaint();
    }//GEN-LAST:event_JbtnBuscarEstudianteActionPerformed

