    private void JbtnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnGuardarCambiosActionPerformed
        // TODO add your handling code here:
        if(!this.JtxtNombre.getText().equalsIgnoreCase("")&& !this.JtxtApellido.getText().equalsIgnoreCase("")&& !this.JtxtCarnet.getText().equalsIgnoreCase("")&& !this.JtxtEdad.getText().equalsIgnoreCase("")&& !this.JtxtTelefono.getText().equalsIgnoreCase("")&& !this.JtxtDireccion.getText().equalsIgnoreCase("")&& !this.JtxtSexo.getText().equalsIgnoreCase("")){
        this.es=new Estudiante(this.JtxtNombre.getText(), this.JtxtApellido.getText(), this.JtxtCarnet.getText(), this.JtxtEdad.getText(), this.JtxtTelefono.getText(), this.JtxtDireccion.getText(), this.JtxtSexo.getText());
        admin.modificarEstudiante(es,this.rutaArchivo,this.nombreArchivo);
        }else{
        
        JOptionPane.showMessageDialog(null, "Existen campos que son obligatorios\n y no pueden estar vacios .-");
        }
        
        
        
    }//GEN-LAST:event_JbtnGuardarCambiosActionPerformed

    private void JbtnBuscarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnBuscarImagenActionPerformed
        // TODO add your handling code here:
        
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("Archivos Permitidos: ",new String []{"png", "jpg", "jpeg", "gif", "bmp"});
        try{
        dlgFilechooser=new JFileChooser();
        dlgFilechooser.setFileFilter(filtro);
        int resultado;
        resultado = dlgFilechooser.showOpenDialog(this);
        if(resultado==JFileChooser.APPROVE_OPTION){
        this.nombreArchivo=dlgFilechooser.getSelectedFile().getName();
        this.rutaArchivo=dlgFilechooser.getSelectedFile().getAbsolutePath();
        this.pictureBoxImagenEstudiante.LoadImage(rutaArchivo);
        this.pictureBoxImagenEstudiante.repaint();
        
        }
        }catch(Exception ex){
        JOptionPane.showMessageDialog(this, "Error al intentar cargar la Imagen: "+ex.getMessage());
        }
    }//GEN-LAST:event_JbtnBuscarImagenActionPerformed

