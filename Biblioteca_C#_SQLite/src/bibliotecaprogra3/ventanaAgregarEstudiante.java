    private void JbtnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnGuardarActionPerformed
        // TODO add your handling code here:
        Estudiante es=new Estudiante(this.JtxtNombre.getText(), this.JtxtApellido.getText(), this.JtxtCarnet.getText(), this.jtxtFecha.getText(), this.JtxtTelefono.getText(), this.JtxtDireccion.getText(), this.JcomboSexo.getSelectedItem().toString());
        
        if(!es.getCarnet().equalsIgnoreCase("") && !es.getNombre().equalsIgnoreCase("") && !es.getApellido().equalsIgnoreCase("") && !es.getSexo().equalsIgnoreCase("") && !es.getDireccion().equalsIgnoreCase("") && !es.getFechaNacimiento().equalsIgnoreCase("") && !es.getSexo().equalsIgnoreCase("") ){
        AdministradorDatos admin=new AdministradorDatos();
        admin.agregarEstudiante(es,this.rutaArchivo,this.nombreArchivo);
        
        }else{
        
           JOptionPane.showMessageDialog(null, "Existen campos que no pueden dejarse en blaco intentelo de nuevo.-"); 
        }
        
        
    }//GEN-LAST:event_JbtnGuardarActionPerformed

    private void JbtnSubirFotoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnSubirFotoActionPerformed
        // TODO add your handling code here:
  FileNameExtensionFilter filtro=new FileNameExtensionFilter("Archivos Permitidos: ",new String []{"png", "jpg", "jpeg", "gif", "bmp"});
        try{
        dlgFilechooser=new JFileChooser();
        dlgFilechooser.setFileFilter(filtro);
        int resultado;
        resultado = dlgFilechooser.showOpenDialog(this);
        if(resultado==JFileChooser.APPROVE_OPTION){
        this.nombreArchivo=dlgFilechooser.getSelectedFile().getName();
        this.rutaArchivo=dlgFilechooser.getSelectedFile().getAbsolutePath();
        this.pictureBoxImagenEstudiante.LoadImage(rutaArchivo);
        this.pictureBoxImagenEstudiante.repaint();
        
        }
        }catch(Exception ex){
        JOptionPane.showMessageDialog(this, "Error al intentar cargar la Imagen: "+ex.getMessage());
        }
    }//GEN-LAST:event_JbtnSubirFotoActionPerformed

