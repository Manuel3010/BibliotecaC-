    private void JbtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbtnRegistrarActionPerformed
        // TODO add your handling code here:
        String tipoUsuario=JtxtUsuario.getText();
        
        if(tipoUsuario.equalsIgnoreCase("administrador")){
            this.vtPrincipal.desbloquer(1);
        }else if(tipoUsuario.equalsIgnoreCase("Encargado")){
            this.vtPrincipal.desbloquer(2);
        }else{
            JOptionPane.showMessageDialog(null, "Error en el tipo de usuario introducido..\n"
                    + "intentelo de nuevo.");
        }
        this.doDefaultCloseAction();
        
    }//GEN-LAST:event_JbtnRegistrarActionPerformed

